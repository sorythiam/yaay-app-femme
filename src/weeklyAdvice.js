// HELPER : récupérer le conseil pour une SA donnée
// =====================================================
export function getAdviceForWeek(weeks, lang = 'fr') {
  // Borner entre 1 et 40
  const w = Math.max(1, Math.min(40, Math.floor(weeks)))
  const advice = weeklyAdvice[w] || weeklyAdvice[1]
  return {
    week: w,
    nutrition: advice.nutrition[lang] || advice.nutrition.fr,
    physical: advice.physical[lang] || advice.physical.fr,
    babyFact: advice.babyFact[lang] || advice.babyFact.fr,
    alert: advice.alert[lang] || advice.alert.fr,
    trimester: w <= 13 ? 'T1' : w <= 27 ? 'T2' : 'T3'
  }
}

// Liste complète pour navigation
export function getAllWeeks() {
  return Object.keys(weeklyAdvice).map(w => parseInt(w)).sort((a, b) => a - b)
}